class StringArrayInput {
    public static void main(String[] args) {

        String[] arr = { "Ram", "Shyam", "Ghanshyam", "Ravan", "Indrajeet" };

        System.out.println("Names are : ");

        for (int i = 0; i <= arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}